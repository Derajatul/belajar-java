public class HelloWorld {
    public static void main(String args[]) {
        // String variable
        String name = "Nama saya dera";
        System.out.println(name);
        
        // Int variable
        int myAge = 15;
        myAge = 18;
        System.out.println("Umur saya " + myAge + " tahun");
        
        // float variable
        float myWeight = 45.5f;
        System.out.println(myWeight);
        
        // char varirable
        char myLetter = 'D';
        System.out.println(myLetter);
        
        // boolean variable
        boolean isStudy = true;
        System.out.println(isStudy);
        
        // final variable
        final int myHeight = 170;
        System.out.println(myHeight);
        
        // Declare many variable
        int x = 5, y=10,z=20;
        System.out.println(x+y+z);
        
    }
}
